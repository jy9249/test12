package java02;

public class jv02_14_비교연산자 {
    
    public static void main(String[] args) {

        // 정수 변수 선언 & 초기화
        int x = 4;
        int y = 5;
        
        System.out.println(x == y);
        System.out.println(x != y);
        System.out.println(x >  y);
        System.out.println(x <  y);
        System.out.println(x >= y);
        System.out.println(x <= y);
        
    }
    
}
